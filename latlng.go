var (
	MAP_CITY map[string]map[string]string
)

func init() {
	err := json.Unmarshal([]byte(JSON_CHINA_LAT_LNG), &MAP_CITY)
	if err != nil {
		panic(err)
	}
}

func GetLatLng(province, city string) string {
	city_loc, found := MAP_CITY[province]
	if !found {
		return DEFAULT_LOC
	}

	loc, found2 := city_loc[city]
	if !found2 {
		loc, found2 = city_loc[province]
		if !found2 {
			return DEFAULT_LOC
		} else {
			return loc
		}
	} else {
		return loc
	}
}

